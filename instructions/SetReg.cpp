#include "../Chip8.h"
#include "SetReg.h"

// %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
SetReg::SetReg(uint16_t opcode)
    :theReg(SECOND_NIBBLE(opcode)),
     theValue(LS_BYTE(opcode))
{
}
// %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

// %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
void SetReg::Execute(Chip8* chip8)
{
    chip8->SetVReg(theReg, theValue);
    
}
